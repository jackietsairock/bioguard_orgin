                                <li>
                                    <h2>10.	Imerslund-Gräsbeck症候群 </h2>
                                    <h4>Imerslund-Gräsbeck syndrome</h4>
                                    </br>
                                    <p>Imerslund-Gräsbeck症候群(IGS)，也稱為鈷胺素吸收不良，鈷胺素又稱為維生素B12，顧名思義是無法吸收維生素B12的病症。維生素B12通常是通過小腸吸取，但此病症患犬不能吸收維生素B12而造成缺乏；幼犬出生時需一定量的維生素B12，一般IGS的幼犬出生後6-12週之內，一旦維生素B12存儲耗盡後，會表現出貧血、嗜睡、食慾不振等症狀。雖然IGS不能治癒，但這種疾病可以透過鈷胺素(維生素B12)的定期補充維持改善。</p>
                                </li>
                                <li>
                                    <h2>11.	Musladin-Lueke 症候群 </h2>
                                    <h4>Musladin-Lueke Syndrome</h4>
                                    </br>
                                    <p>Musladin-Lueke 症候群 (MLS)是米格魯小獵犬(Beagle)特有的體染色體隱性遺傳疾病，此種基因突變會影響犬隻皮膚和結締組織的正常發育，造成外觀和步態的異常。</p>
                                    </br>
                                    <p>其他的症狀包括，粗糙厚實的皮膚、歪斜的眼神、類似芭蕾舞姿的前肢步態，和僵硬的關節等。以上症狀雖都不至於危及生命，但臨床上MLS患犬卻可能有癲癇發作的狀況，需特別注意。經由獸醫師的指導照護，還是可以幫助患犬維持好的生活品質。    </p>
                                </li>
                                <li>
                                    <h2>12.	新生兒小腦皮質病變 </h2>
                                    <h4>Neonatal Cerebellar Cortical Degeneration</h4>
                                    </br>
                                    <p>新生兒小腦皮質病變(NCCD)是一種神經系統疾病，影響平衡和協調。該疾病損害小腦與調節運動的大腦區域，造成小腦干擾平衡與功能障礙，導致動作異常。</p>
                                    </br>
                                    <p>NCCD是一個早發性疾病，患犬出生時為正常，該疾病的跡象在出生後三週內出現，症狀包括行為不協調。疾病的進展可能導致全身震顫和癲癇樣發作，不幸的是，目前沒有這種疾病的治療辦法，然而NCCD是隱性遺傳基因疾病，若能在繁殖之前檢驗，方可避免遺傳風險。</p>
                                </li>
                                <li>
                                    <h2>13.	骨發生不全 </h2>
                                    <h4>Osteogenesis Imperfecta</h4>
                                    </br>
                                    <p>骨發生不全(OI)是一種體染色體隱性遺傳疾病，又稱犬脆骨病。該基因突變損害第一型膠原蛋白的正常發育，導致骨骼和牙齒薄而脆。雖然骨頭是正常的長度，但由於低骨密度造成容易破碎或斷裂，一旦骨骼發生損傷時，將造成癒合緩慢或癒合不完整。除了易脆的牙齒和骨頭，OI的症狀也包括肌肉、肌腱無力、脊柱彎曲、和聽力受損。在嚴重的情況下，受影響的小狗將必須處以安樂死，而成骨不全症最常見的有臘腸犬(Doxie)、米格魯(Beagle)。</p>
                                </li>
                                <li>
                                    <h2>14.	原發廣角性青光眼  </h2>
                                    <h4>Primary Open Angle Glaucoma</h4>
                                    </br>
                                    <p>原發廣性青光眼(POAG)是體染色體隱性遺傳疾病，為眼內壓升高，造成眼睛疼痛不適和視力減退，此遺傳缺陷妨礙了眼房液的正常釋放，兩眼壓力增大，導致視網膜神經節細胞死亡。視網膜神經節是接收來自視網膜，然後通過視神經到大腦傳送視覺信息的神經細胞，一旦視網膜神經節細胞已經死亡，不會再生，便導致視力喪失。在病犬8個月到一歲半之間時，症狀開始出現，眼睛可能出現泛紅、疼痛，如果不能快速診斷和解決病症，則可能導致視力喪失。 原發性青光眼發生在許多犬種，包括美國可卡犬(Cocker Spaniel)，沙皮犬(Shar Pei)，傑克羅素梗犬(Jack Russell Terrier)。</p>
                                </li>
                                <li>
                                    <h2>15.	魚鱗癬</h2>
                                    <h4>Ichthyosis</h4>
                                    </br>
                                    <p>魚鱗癬是一種體染色體隱性遺傳，為黃金獵犬( GOLDEN RETRIEVER )特有的遺傳性疾病。此疾病造成患犬表皮結構異常，導致皮膚暗沉、粗糙和過多皮屑的產生。</p>
                                    </br>
                                    <p>臨床上常見患犬皮膚有過度角化的現象，皮屑過度剝落，趾部、腕部、踝部增硬增厚，且有色素沉澱產生，症狀輕微至嚴重。此疾病好發時間不一，發病後預後的好壞取卻於患犬的賀爾蒙分泌狀況和環境的維護。</p>
                                    </br>
                                    <p>魚鱗癬雖然不會對犬隻造成生命危險，但卻嚴重影響犬隻的外觀和生活品質，且常伴隨著繼發性的感染問題，如真菌感染和酵母菌感染。患犬大多需要透過長期洗藥浴和服用抗生素的方式維持皮膚的健康。</p>
                                </li>
                                <li>
                                    <h2>16.	肌肉萎縮症 </h2>
                                    <h4>Muscular Dystrophy</h4>
                                    </br>
                                    <p>肌肉萎縮症(MD)是性聯隱性基因遺傳疾病，為黃金獵犬( GOLDEN RETRIEVER )特有的遺傳性疾病。起因於製造肌縮蛋白的基因發生缺損或突變，導致肌縮蛋白減少，造成犬隻骨骼肌和心肌漸進性萎縮。</p>
                                    </br>
                                    <p>此疾病好發於犬隻1-2月齡，臨床症狀常見骨骼肌萎縮，四肢無力，吞嚥困難等。其中，臨床症狀的好惡取決於肌肉萎縮程度。在患犬6月齡時死亡率達到最高。</p>
                                </li>
                                <li>
                                    <h2>17.	新生幼犬腦病 </h2>
                                    <h4>Neonatal Encephalopathy</h4>
                                    </br>
                                    <p>新生幼犬腦病(NE)是常見的體染色體隱性遺傳疾病，好發於貴賓幼犬。患犬出生時虛弱，出生不到一週便死亡。倖存活過一週的犬隻，常見症狀為共濟失調，全身震顫，嚴重的全身痙攣，且小腦嚴重萎縮。目前尚無存活過7週齡的犬隻。</p>
                                </li>
                                <li>
                                    <h2>18.	丙酮酸活化酶缺乏症 </h2>
                                    <h4>Pyruvate Kinase Deficiency</h4>
                                    </br>
                                    <p>丙酮酸活化酶是動物體內参與新陳代謝的重要酵素，患有PKD的動物因丙酮酸活化酶的缺乏，會進一步導致體內紅血球的死亡，造成嚴重的溶血性貧血。在臨床上，常見患犬飽受慢性貧血之苦。</p>
                                    </br>
                                    <p>此病症好發於犬隻4個月至１歲的時期，常見症狀如：精神不振、心搏速率增加、心臟雜音、牙齦蒼白、和發育不良……等。隨著病情的發展，骨骼和肝臟亦會受到影響，最終導致死亡。</p>
                                    </br>
                                    <p>目前沒有針對此病症的治療方式，能做的僅是給予患犬對症治療。</p>
                                    </br>
                                    <p>丙酮酸活化酶缺乏症(PKD)是一種體染色體隱性遺傳，故帶有單一缺陷基因的犬隻不會出現臨床症狀，若能在配種前預先做好缺陷基因的排除，是較為保險的。</p>
                                    </br>
                                    <p>常見相關品種包括臘腸犬(Dachshunds)，拉布拉多獵犬(Labrador Retrievers)，哈巴狗(Pugs)，比格犬(Beagles)，山梗(Cairn Terries)和西高地白梗(West Highland White Terriers)。</p>
                                </li>
                                <li>
                                    <h2>19.	少年型遺傳性白內障 </h2>
                                    <h4>Juvenile Hereditary Cataracts</h4>
                                    </br>
                                    <p>白內障是指位於瞳孔後方的水晶體或包覆水晶體的囊袋出現不等程度的混濁現象，導致動物不同程度的視覺障礙。在犬，白內障是常見家族性遺傳疾病，被稱為遺傳性白內障(Hereditary Cataracts)。</p>
                                    </br>
                                    <p>造成遺傳性白內障的原因，主要是由HSF4基因上的突變所引起，導致多個品種的犬隻產生這類型的疾病。此疾病通常是雙側性的，且好發時間早，與HSF4相關的白內障也可能發生在水晶體的後部區域。其病程可能緩慢不至影響犬隻視力，亦有可能發展快速，導致視力在短時間內完全喪失。矯正手術雖行之有年，但費用昂貴且成效不彰。</p>
                                    </br>
                                    <p>常見相關品種有波士頓梗(Boston Terriers)，斯塔福郡鬥牛梗(Stafford Bull Terriers)，和法國鬥牛犬(French Bulldogs)。</p>
                                    </br>
                                    <p>然而，不是所有的白內障都是因突變所致。形成的原因也可以僅因年紀或營養缺乏所致。</p>
                                </li>
                                <li>
                                    <h2>20.	犬遺傳性視網膜退化症 </h2>
                                    <h4>Progressive Retinal Atrophy</h4>
                                    </br>
                                    <p>犬遺傳性視網膜退化症(PRA)，泛指廣泛發生在許多犬種之遺傳性或後天性眼睛視網膜功能退化的疾病，最終會導致失明。主要發生於純種犬，已知至少超過百種犬帶有此類遺傳疾病。</p>
                                    </br>
                                    <p>PRA是一種漸進的疾病。亦屬雙側性的疾病，即兩眼均會發病即使病況不一。導致PRA 病症發生的主要因素是那些和視覺影像生成相關的基因發生自然突變, 網膜光受體上的某個光傳導蛋白酵素功能缺陷，之後代代相承加上近親繁殖所造成。</p>
                                    </br>
                                    <p>PRA有許多不同類型，特定犬種有著不同類型的PRA，這是因為各犬種有特定的基因突變所造成不同的蛋白酵素功能缺陷；然而，無論這基因之蛋白酵素缺陷有多麼特殊，所有不同類型的PRA終將導致桿狀細胞(rod cell)和錐狀細胞(cone cell)的萎縮。</p>
                                    </br>
                                    <p>眼球的內表面有一層很薄的神經膜，稱之為視網膜，如同照像機的底片一樣，是眼球內很重要的部分，主要是視網膜上的光感應細胞在接收來自眼球其他結構所匯集集中的光線後，轉換成電神經信號，藉由視神經，在大腦解開成影像。視網膜光感應細胞包含桿狀細胞(rod)和錐狀細胞(cone)，桿狀細胞主司黑白視力，而錐狀細胞主司顏色。</p>
                                    </br>
                                    <p>此類眼疾是因膜上的光感應細胞退化引發視力衰退所導致；大多在視網膜的桿狀細胞(rod cell)先發生病變導致夜盲症，並且逐步地在錐狀細胞(cone cell)發生病變，造成視力逐步喪失，最終導致失明。</p>
                                </li>
                            </ul>
