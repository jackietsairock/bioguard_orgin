                            <ul>
                                <li>
                                    <h2>1.	原發性水晶體脫位　</h2>
                                    <h4>Primary Lens Luxation</h4>
                                    </br>
                                    <p>水晶體脫位(PLL)，其特徵是睫狀纖維的減弱，最終導致水晶體脫位的疾病。在犬的眼睛，水晶體位於虹膜和瞳孔後方並負責使光聚焦到眼睛後方的視網膜。睫狀纖維是負責使晶狀體保持於正確的位置。在PLL的犬隻中，眼睛的睫狀纖維較弱，使水晶體無法保持在正確的位置或脫落。
                                        PLL的犬隻，眼部通常會非常疼痛，大多都會導致永久失明，這些症狀通常會發生在3-6歲之間。一般會建議，若狗狗家族中有出現PLL者，應請獸醫持續監控眼睛的狀況並進行評估。</p>
                                    </br>
                                    <p>原發性水晶體脫位是一種體染色體隱性遺傳的疾病，也就是說，必須要同一對基因皆帶有此疾病的基因者才會發病，但目前也曾在只帶單一一個基因的帶原者上出現的紀錄。帶原者是指一對基因中，只帶有一個異常突變基因者，在隱性遺傳疾病的帶原者中，
                                        通常不會出現臨床症狀，但若父母皆為帶原者，出生的動物就會有四分之一的機率成為PLL的患者。但並不是所有的水晶體脫位皆為PLL造成，可能造成脫位的原因也可能是外傷或其他感染的疾病。</p>
                                </li>
                                <li>
                                    <h2>2.	退化性脊髓神經病變 </h2>
                                    <h4>Degenerative Myelopathy</h4>
                                    </br>
                                    <p>退化性脊髓神經病變(DM)是一種會逐漸惡化的神經系統疾病，常發生在柯基犬(Corgi)、拳師(Boxer)和伯恩山犬(Bernese Mountain Dog)發生。在一對基因中皆帶有異常基因的犬隻，會造成脊髓神經細胞的破壞，影響大腦與脊髓間的訊息傳遞，最終出現臨床神經症狀。
                                        若罹患此疾病，一開始常可以觀察到動物的步態不穩，走路搖晃，隨著病情的發展，犬隻前腿會逐漸無法行走，且無法正常維持膀胱功能，不過唯一幸運的是，此疾病並不會造成劇烈的疼痛感。</p>
                                    </br>
                                    <p>退化性脊髓神經病變一般發生的時機較晚，平均年齡約在8歲左右，但目前的研究發現，在有些一對基因皆帶有異常基因的犬隻，仍然沒有發病。因此，目前懷疑，此疾病的發生，在不同品種的犬隻身上，並不一定完全都與基因型有直接相關，仍待更多的研究。</p>
                                </li>
                                <li>
                                    <h2>3.	高尿酸尿症　</h2>
                                    <h4>Hyperuricosuria</h4>
                                    </br>
                                    <p>高尿酸尿症(HUU)是一種體染色體隱性遺傳泌尿疾病，但目前臨床上也有發現帶有HUU基因的犬隻並未出現臨床症狀的病例。</p>
                                    </br>
                                    <p>犬隻若有此對基因的突變，會使體內的物質代謝成尿酸，尿酸濃度過高，容易在膀胱中形成結石，當結石隨著尿液從膀胱流經尿道時，可能會造成疼痛和發炎。
                                        家中的狗狗若有出現排尿困難或膀胱發炎的症狀，都可能與HUU有關，其他症狀包括尿中帶血或頻尿。若尿酸結石卡在尿道中，無法順利排出，則需進行手術將結石移除，若結石造成尿路完全阻塞，則可能會造成動物生命危險，
                                        應謹慎以對。引此，育種前建議先進行基因評估的檢測。
                                    </p>
                                </li>
                                <li>
                                    <h2>4.	肌小管病變 </h2>
                                    <h4>Centronuclear Myopathy</h4>
                                    </br>
                                    <p>肌小管病變(CNM)過去又被稱為遺傳性肌病(Hereditary myopathy)，是一種體染色體隱性突變的疾病，常導致拉布拉多犬肌肉功能不足。</p>
                                    </br>
                                    <p>幼犬剛出生時外表通常無異狀，但很快的症狀便會開始變得明顯。小狗可能因食道肌肉張力不足，導至體重一直遲遲無法增加，在2-5個月，症狀通常都會逐漸明顯，出現的症狀包括失去肌肉張力、無法正常控制肌肉、異常的步態和運動不耐，且症狀會隨著氣溫降低而惡化。</p>
                                    </br>
                                    <p>不幸的是，目前針對CNM並沒有治療的方法，且罹患CNM的狗，壽命並不會因此明顯變短，因此，狗狗將終身受此疾病的困擾。</p>
                                </li>
                                <li>
                                    <h2>5.	運動誘發虛脫症 </h2>
                                    <h4>Exercise-Induced Collapse</h4>
                                    </br>
                                    <p>運動誘發虛脫症(EIC)是一種體染色體隱性遺傳性疾病，在帕金獵犬(Boykin Spaniel)、乞沙比克海灣獵犬(Chesapeake Bay Retriever)、拉布拉多(Labrador Retriever)等品種較常見。通常是指因激烈運動後，肌肉出現無法控制的症狀。有此對基因突變的狗，
                                        會在犬隻約5個月大至3歲間陸續出現症狀，可能與這段期間是狗活動力大幅增加的時期有關。</p>
                                    </br>
                                    <p>EIC的症狀，一般會在5-25分鐘激烈運動後出現。並非所有類型的運動都可誘發EIC， 一般來說，犬隻必須激烈奔跑很長一段時間才會出現EIC的症狀，一開始可能會出現搖擺頭部，後腿變得逐漸無力，最終虛脫。EIC發作的嚴重程度不同，也和品種不同有關，
                                        有些還能掙扎或拖著後腿行走，其他則可能完全無法移動。這些發作通常持續約5-25分鐘，但是在某些較嚴重的病患，狗可能會在發作的當下立即死亡。</p>
                                    <br>
                                    <p>EIC的病患，並不一定會造成生活品質的下降，但主人必須注意哪些活動會造成狗狗引發EIC，需要在生活中避免從事相關的活動。</p>
                                </li>
                                <li>
                                    <h2>6.	遺傳性鼻角化不全症 </h2>
                                    <h4>Hereditary Nasal Parakeratosis</h4>
                                    </br>
                                    <p>遺傳性鼻角化不全症(HNPK)是拉布拉多(Labrador Retriever)的一種體染色體隱性遺傳疾病。這種疾病的症狀一般出現在6至12月齡的狗。</p>
                                    <br>
                                    <p>罹患此疾病的狗，在鼻子的表面和邊緣會出現乾燥、粗糙， 且略帶灰褐色的硬皮。在某些病患中，鼻尖的周圍會出現裂紋，如果不給予治療，可能會造成淺層細菌感染，隨著時間變長，鼻子可能會出現脫色、顏色變淡的狀況。</p>
                                    <br>
                                    <p>雖然此疾病並不會造成立即性的生命危害，但仍須終身照護，避免鼻頭的硬皮過度增生或因裂紋造成的細菌感染。</p>
                                </li>
                                <li>
                                    <h2>7.	惡性高熱 </h2>
                                    <h4>Malignant Hyperthermia</h4>
                                    </br>
                                    <p>惡性高熱(MH)是一種體染色體顯性遺傳疾病，在邊境牧羊犬(Border Collie)、史賓格犬(English Springer Spaniel)、格雷伊獵犬(Greyhound)、愛爾蘭獵狼犬(Irish Wolfhound)和拉布拉多 (Labrador Retriever)較常見。罹患此疾病的狗，會因為接觸到某些觸發因子，出現危害生命的生理反應。觸發因子可能包括某些特定的藥物，其中最常見的就是吸入性麻醉劑〈如halothane〉、食品成分〈如咖啡因和啤酒花〉或過度運動。MH又被稱為犬隻緊迫症(canine stress syndrome)，即當犬隻在緊迫下或受到過度刺激時也可能會出現。當犬隻有MH基因突變時，手術麻醉時若接觸到會觸發MH的麻醉藥物或其他藥物，可能會有死亡的風險。</p>
                                    </br>
                                    <p>雖然並無針對MH的治療，但可藉由確認犬隻是否帶有MH的突變基因，避免術前或術中接觸到可能觸發MH的藥物，進而避免MH的發生。除此之外，也可藉由避免過度運動或處於過度緊迫的環境，避免MH的發生。</p>
                                </li>
                                <li>
                                    <h2>8.	神經性蠟樣脂褐質沈著症 </h2>
                                    <h4>Neuronal Ceroid Lipofuscinosis</h4>
                                    </br>
                                    <p>神經性蠟樣脂褐質沈著症(NCL)為體染色體隱性遺傳疾病，是一種致命的神經系統疾病，其致病基因導致神經元中產生有缺陷的蛋白質，造成大腦組織內部廢物堆積，進而造成神經元的功能受到破壞。這種遺傳疾病出現在英國雪達犬（Irish Setter）、邊境牧羊犬（Border Collie）。臨床症狀包括行為的改變，例如侵略、抑鬱、遲鈍、學習行為的喪失、共濟失調、震顫、四肢麻痺等。也因為病情會逐漸惡化，致使藥物仍無法改善治療，患犬一般於三歲左右死亡。</p>
                                </li>
                                <li>
