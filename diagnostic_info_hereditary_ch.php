                            <ul>
                                <li>
                                    <h2>1.	貓多囊性腎病  </h2>
                                    <h4>Polycystic Kidney Disease</h4>
                                    </br>
                                    <p>貓的多囊性腎病(PKD)大多發生在長毛貓，像是波斯貓(Persians)或是有波斯貓血統(persian-related cats)的貓 (例：金吉拉)身上，是貓最常見的遺傳性疾病。有問題的貓，會在腎皮質及髓質出現各種不同大小而且充滿液體的囊腫，故俗稱泡泡腎。這些充滿液體的囊會漸漸地壓迫腎臟的其他組織造成腎臟組織壞死而形成慢性腎功能衰竭。</p>
                                    </br>
                                    <p>這些從貓一出生就出現的腎臟囊腫，會隨著年齡增加而變大，最後會壓迫腎臟實質造成不可回復性的腎衰竭。有些貓甚至連肝臟、胰臟及子宮都可以見到這些囊腫。</p>
                                    </br>
                                    <p>罹患多囊性腎臟疾病的貓咪，雖然一出生就有不正常的腎臟，但是大多要到3-10歲之間(平均是7歲)才會發現症狀，少數情況嚴重的幼貓，會在2個月大之前就死亡。</p>
                                    </br>
                                    <p>造成貓多囊腎病的原因，主要是由貓PKD1基因上的點突變所引起的，是一種顯性遺傳疾病，只會由親代傳給子代，貓與貓之間接觸並不會傳染此疾病。一般來說，多囊腎病主要發生在長毛貓身上，研究顯示，有高達38%的波斯貓帶有異常的PKD1基因。</p>
                                    </br>
                                    <p>主要侵犯波斯及波斯相關品種的貓，例如喜馬拉雅貓和異國短毛貓，但也有報告指出其他品種的貓，例如布偶貓、蘇格蘭摺耳貓，或是其他短毛貓品種，也有可能有這種遺傳疾病。</p>
                                    </br>
                                    <p>病貓可能會食慾減低，體重減輕且會精神沉鬱和缺乏活力。有些貓甚至會有嘔吐或是口渴程度增加引發頻尿。嚴重時可能會有運動失調 (Ataxia)或是神經系統問題 (抽搐或痙孿)。</p>
                                </li>
