                                <li>
                                    <h2>1.	猫多囊性肾病  </h2>
                                    <h4>Polycystic Kidney Disease</h4>
                                    </br>
                                    <p>猫的多囊性肾病(PKD)大多发生在长毛猫，像是波斯猫(Persians)或是有波斯猫血统(persian-related cats)的猫 (例：金吉拉)身上，是猫最常见的遗传性疾病。有问题的猫，会在肾皮质及髓质出现各种不同大小而且充满液体的囊肿，故俗称泡泡肾。这些充满液体的囊会渐渐地压迫肾脏的其他组织造成肾脏组织坏死而形成慢性肾功能衰竭。</p>
                                    </br>
                                    <p>这些从猫一出生就出现的肾脏囊肿，会随着年龄增加而变大，最後会压迫肾脏实质造成不可回复性的肾衰竭。有些猫甚至连肝脏丶胰脏及子宫都可以见到这些囊肿。</p>
                                    </br>
                                    <p>罹患多囊性肾脏疾病的猫咪，虽然一出生就有不正常的肾脏，但是大多要到3-10岁之间(平均是7岁)才会发现症状，少数情况严重的幼猫，会在2个月大之前就死亡。</p>
                                    </br>
                                    <p>造成猫多囊肾病的原因，主要是由猫PKD1基因上的点突变所引起的，是一种显性遗传疾病，只会由亲代传给子代，猫与猫之间接触并不会传染此疾病。一般来说，多囊肾病主要发生在长毛猫身上，研究显示，有高达38%的波斯猫带有异常的PKD1基因。</p>
                                    </br>
                                    <p>主要侵犯波斯及波斯相关品种的猫，例如喜马拉雅猫和异国短毛猫，但也有报告指出其他品种的猫，例如布偶猫丶苏格兰摺耳猫，或是其他短毛猫品种，也有可能有这种遗传疾病。</p>
                                    </br>
                                    <p>病猫可能会食欲减低，体重减轻且会精神沉郁和缺乏活力。有些猫甚至会有呕吐或是口渴程度增加引发频尿。严重时可能会有运动失调 (Ataxia)或是神经系统问题 (抽搐或痉孪)。</p>
                                </li>
