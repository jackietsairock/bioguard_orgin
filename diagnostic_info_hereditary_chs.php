                                <li>
                                    <h2>10.	Imerslund-Gräsbeck症候群 </h2>
                                    <h4>Imerslund-Gräsbeck syndrome</h4>
                                    </br>
                                    <p>Imerslund-Gräsbeck症候群(IGS)，也称为钴胺素吸收不良，钴胺素又称为维生素B12，顾名思义是无法吸收维生素B12的病症。维生素B12通常是通过小肠吸取，但此病症患犬不能吸收维生素B12而造成缺乏；幼犬出生时需一定量的维生素B12，一般IGS的幼犬出生後6-12周之内，一旦维生素B12存储耗尽後，会表现出贫血丶嗜睡丶食欲不振等症状。虽然IGS不能治愈，但这种疾病可以透过钴胺素(维生素B12)的定期补充维持改善。</p>
                                </li>
                                <li>
                                    <h2>11.	Musladin-Lueke 症候群 </h2>
                                    <h4>Musladin-Lueke Syndrome</h4>
                                    </br>
                                    <p>Musladin-Lueke 症候群 (MLS)是米格鲁小猎犬(Beagle)特有的体染色体隐性遗传疾病，此种基因突变会影响犬只皮肤和结缔组织的正常发育，造成外观和步态的异常。</p>
                                    </br>
                                    <p>其他的症状包括，粗糙厚实的皮肤丶歪斜的眼神丶类似芭蕾舞姿的前肢步态，和僵硬的关节等。以上症状虽都不至於危及生命，但临床上MLS患犬却可能有癫痫发作的状况，需特别注意。经由兽医师的指导照护，还是可以帮助患犬维持好的生活品质。    </p>
                                </li>
                                <li>
                                    <h2>12.	新生儿小脑皮质病变 </h2>
                                    <h4>Neonatal Cerebellar Cortical Degeneration</h4>
                                    </br>
                                    <p>新生儿小脑皮质病变(NCCD)是一种神经系统疾病，影响平衡和协调。该疾病损害小脑与调节运动的大脑区域，造成小脑干扰平衡与功能障碍，导致动作异常。</p>
                                    </br>
                                    <p>NCCD是一个早发性疾病，患犬出生时为正常，该疾病的迹象在出生後三周内出现，症状包括行为不协调。疾病的进展可能导致全身震颤和癫痫样发作，不幸的是，目前没有这种疾病的治疗办法，然而NCCD是隐性遗传基因疾病，若能在繁殖之前检验，方可避免遗传风险。</p>
                                </li>
                                <li>
                                    <h2>13.	骨发生不全 </h2>
                                    <h4>Osteogenesis Imperfecta</h4>
                                    </br>
                                    <p>骨发生不全(OI)是一种体染色体隐性遗传疾病，又称犬脆骨病。该基因突变损害第一型胶原蛋白的正常发育，导致骨骼和牙齿薄而脆。虽然骨头是正常的长度，但由於低骨密度造成容易破碎或断裂，一旦骨骼发生损伤时，将造成愈合缓慢或愈合不完整。除了易脆的牙齿和骨头，OI的症状也包括肌肉丶肌腱无力丶脊柱弯曲丶和听力受损。在严重的情况下，受影响的小狗将必须处以安乐死，而成骨不全症最常见的有腊肠犬(Doxie)丶米格鲁(Beagle)。</p>
                                </li>
                                <li>
                                    <h2>14.	原发广角性青光眼  </h2>
                                    <h4>Primary Open Angle Glaucoma</h4>
                                    </br>
                                    <p>原发广性青光眼(POAG)是体染色体隐性遗传疾病，为眼内压升高，造成眼睛疼痛不适和视力减退，此遗传缺陷妨碍了眼房液的正常释放，两眼压力增大，导致视网膜神经节细胞死亡。视网膜神经节是接收来自视网膜，然後通过视神经到大脑传送视觉信息的神经细胞，一旦视网膜神经节细胞已经死亡，不会再生，便导致视力丧失。在病犬8个月到一岁半之间时，症状开始出现，眼睛可能出现泛红丶疼痛，如果不能快速诊断和解决病症，则可能导致视力丧失。 原发性青光眼发生在许多犬种，包括美国可卡犬(Cocker Spaniel)，沙皮犬(Shar Pei)，杰克罗素梗犬(Jack Russell Terrier)。</p>
                                </li>
                                <li>
                                    <h2>15.	鱼鳞癣</h2>
                                    <h4>Ichthyosis</h4>
                                    </br>
                                    <p>鱼鳞癣是一种体染色体隐性遗传，为黄金猎犬( GOLDEN RETRIEVER )特有的遗传性疾病。此疾病造成患犬表皮结构异常，导致皮肤暗沉丶粗糙和过多皮屑的产生。</p>
                                    </br>
                                    <p>临床上常见患犬皮肤有过度角化的现象，皮屑过度剥落，趾部丶腕部丶踝部增硬增厚，且有色素沉淀产生，症状轻微至严重。此疾病好发时间不一，发病後预後的好坏取却於患犬的贺尔蒙分泌状况和环境的维护。</p>
                                    </br>
                                    <p>鱼鳞癣虽然不会对犬只造成生命危险，但却严重影响犬只的外观和生活品质，且常伴随着继发性的感染问题，如真菌感染和酵母菌感染。患犬大多需要透过长期洗药浴和服用抗生素的方式维持皮肤的健康。</p>
                                </li>
                                <li>
                                    <h2>16.	肌肉萎缩症 </h2>
                                    <h4>Muscular Dystrophy</h4>
                                    </br>
                                    <p>肌肉萎缩症(MD)是性联隐性基因遗传疾病，为黄金猎犬( GOLDEN RETRIEVER )特有的遗传性疾病。起因於制造肌缩蛋白的基因发生缺损或突变，导致肌缩蛋白减少，造成犬只骨骼肌和心肌渐进性萎缩。</p>
                                    </br>
                                    <p>此疾病好发於犬只1-2月龄，临床症状常见骨骼肌萎缩，四肢无力，吞咽困难等。其中，临床症状的好恶取决於肌肉萎缩程度。在患犬6月龄时死亡率达到最高。</p>
                                </li>
                                <li>
                                    <h2>17.	新生幼犬脑病 </h2>
                                    <h4>Neonatal Encephalopathy</h4>
                                    </br>
                                    <p>新生幼犬脑病(NE)是常见的体染色体隐性遗传疾病，好发於贵宾幼犬。患犬出生时虚弱，出生不到一周便死亡。幸存活过一周的犬只，常见症状为共济失调，全身震颤，严重的全身痉挛，且小脑严重萎缩。目前尚无存活过7周龄的犬只。</p>
                                </li>
                                <li>
                                    <h2>18.	丙酮酸活化酶缺乏症 </h2>
                                    <h4>Pyruvate Kinase Deficiency</h4>
                                    </br>
                                    <p>丙酮酸活化酶是动物体内参与新陈代谢的重要酵素，患有PKD的动物因丙酮酸活化酶的缺乏，会进一步导致体内红血球的死亡，造成严重的溶血性贫血。在临床上，常见患犬饱受慢性贫血之苦。</p>
                                    </br>
                                    <p>此病症好发於犬只4个月至１岁的时期，常见症状如：精神不振丶心搏速率增加丶心脏杂音丶牙龈苍白丶和发育不良……等。随着病情的发展，骨骼和肝脏亦会受到影响，最终导致死亡。</p>
                                    </br>
                                    <p>目前没有针对此病症的治疗方式，能做的仅是给予患犬对症治疗。</p>
                                    </br>
                                    <p>丙酮酸活化酶缺乏症(PKD)是一种体染色体隐性遗传，故带有单一缺陷基因的犬只不会出现临床症状，若能在配种前预先做好缺陷基因的排除，是较为保险的。</p>
                                    </br>
                                    <p>常见相关品种包括腊肠犬(Dachshunds)，拉布拉多猎犬(Labrador Retrievers)，哈巴狗(Pugs)，比格犬(Beagles)，山梗(Cairn Terries)和西高地白梗(West Highland White Terriers)。</p>
                                </li>
                                <li>
                                    <h2>19.	少年型遗传性白内障 </h2>
                                    <h4>Juvenile Hereditary Cataracts</h4>
                                    </br>
                                    <p>白内障是指位於瞳孔後方的水晶体或包覆水晶体的囊袋出现不等程度的混浊现象，导致动物不同程度的视觉障碍。在犬，白内障是常见家族性遗传疾病，被称为遗传性白内障(Hereditary Cataracts)。</p>
                                    </br>
                                    <p>造成遗传性白内障的原因，主要是由HSF4基因上的突变所引起，导致多个品种的犬只产生这类型的疾病。此疾病通常是双侧性的，且好发时间早，与HSF4相关的白内障也可能发生在水晶体的後部区域。其病程可能缓慢不至影响犬只视力，亦有可能发展快速，导致视力在短时间内完全丧失。矫正手术虽行之有年，但费用昂贵且成效不彰。</p>
                                    </br>
                                    <p>常见相关品种有波士顿梗(Boston Terriers)，斯塔福郡斗牛梗(Stafford Bull Terriers)，和法国斗牛犬(French Bulldogs)。</p>
                                    </br>
                                    <p>然而，不是所有的白内障都是因突变所致。形成的原因也可以仅因年纪或营养缺乏所致。</p>
                                </li>
                                <li>
                                    <h2>20.	犬遗传性视网膜退化症 </h2>
                                    <h4>Progressive Retinal Atrophy</h4>
                                    </br>
                                    <p>犬遗传性视网膜退化症(PRA)，泛指广泛发生在许多犬种之遗传性或後天性眼睛视网膜功能退化的疾病，最终会导致失明。主要发生於纯种犬，已知至少超过百种犬带有此类遗传疾病。</p>
                                    </br>
                                    <p>PRA是一种渐进的疾病。亦属双侧性的疾病，即两眼均会发病即使病况不一。导致PRA 病症发生的主要因素是那些和视觉影像生成相关的基因发生自然突变, 网膜光受体上的某个光传导蛋白酵素功能缺陷，之後代代相承加上近亲繁殖所造成。</p>
                                    </br>
                                    <p>PRA有许多不同类型，特定犬种有着不同类型的PRA，这是因为各犬种有特定的基因突变所造成不同的蛋白酵素功能缺陷；然而，无论这基因之蛋白酵素缺陷有多麽特殊，所有不同类型的PRA终将导致杆状细胞(rod cell)和锥状细胞(cone cell)的萎缩。</p>
                                    </br>
                                    <p>眼球的内表面有一层很薄的神经膜，称之为视网膜，如同照像机的底片一样，是眼球内很重要的部分，主要是视网膜上的光感应细胞在接收来自眼球其他结构所汇集集中的光线後，转换成电神经信号，藉由视神经，在大脑解开成影像。视网膜光感应细胞包含杆状细胞(rod)和锥状细胞(cone)，杆状细胞主司黑白视力，而锥状细胞主司颜色。</p>
                                    </br>
                                    <p>此类眼疾是因膜上的光感应细胞退化引发视力衰退所导致；大多在视网膜的杆状细胞(rod cell)先发生病变导致夜盲症，并且逐步地在锥状细胞(cone cell)发生病变，造成视力逐步丧失，最终导致失明。</p>
                                </li>
                            </ul>
