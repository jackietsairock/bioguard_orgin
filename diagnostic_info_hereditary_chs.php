                                <li>
                                    <h2>1.	原发性水晶体脱位　</h2>
                                    <h4>Primary Lens Luxation</h4>
                                    </br>
                                    <p>水晶体脱位(PLL)，其特徵是睫状纤维的减弱，最终导致水晶体脱位的疾病。在犬的眼睛，水晶体位於虹膜和瞳孔後方并负责使光聚焦到眼睛後方的视网膜。睫状纤维是负责使晶状体保持於正确的位置。在PLL的犬只中，眼睛的睫状纤维较弱，使水晶体无法保持在正确的位置或脱落。
                                        PLL的犬只，眼部通常会非常疼痛，大多都会导致永久失明，这些症状通常会发生在3-6岁之间。一般会建议，若狗狗家族中有出现PLL者，应请兽医持续监控眼睛的状况并进行评估。</p>
                                    </br>
                                    <p>原发性水晶体脱位是一种体染色体隐性遗传的疾病，也就是说，必须要同一对基因皆带有此疾病的基因者才会发病，但目前也曾在只带单一一个基因的带原者上出现的纪录。带原者是指一对基因中，只带有一个异常突变基因者，在隐性遗传疾病的带原者中，
                                        通常不会出现临床症状，但若父母皆为带原者，出生的动物就会有四分之一的机率成为PLL的患者。但并不是所有的水晶体脱位皆为PLL造成，可能造成脱位的原因也可能是外伤或其他感染的疾病。</p>
                                </li>
                                <li>
                                    <h2>2.	退化性脊髓神经病变 </h2>
                                    <h4>Degenerative Myelopathy</h4>
                                    </br>
                                    <p>退化性脊髓神经病变(DM)是一种会逐渐恶化的神经系统疾病，常发生在柯基犬(Corgi)丶拳师(Boxer)和伯恩山犬(Bernese Mountain Dog)发生。在一对基因中皆带有异常基因的犬只，会造成脊髓神经细胞的破坏，影响大脑与脊髓间的讯息传递，最终出现临床神经症状。
                                        若罹患此疾病，一开始常可以观察到动物的步态不稳，走路摇晃，随着病情的发展，犬只前腿会逐渐无法行走，且无法正常维持膀胱功能，不过唯一幸运的是，此疾病并不会造成剧烈的疼痛感。</p>
                                    </br>
                                    <p>退化性脊髓神经病变一般发生的时机较晚，平均年龄约在8岁左右，但目前的研究发现，在有些一对基因皆带有异常基因的犬只，仍然没有发病。因此，目前怀疑，此疾病的发生，在不同品种的犬只身上，并不一定完全都与基因型有直接相关，仍待更多的研究。</p>
                                </li>
                                <li>
                                    <h2>3.	高尿酸尿症　</h2>
                                    <h4>Hyperuricosuria</h4>
                                    </br>
                                    <p>高尿酸尿症(HUU)是一种体染色体隐性遗传泌尿疾病，但目前临床上也有发现带有HUU基因的犬只并未出现临床症状的病例。</p>
                                    </br>
                                    <p>犬只若有此对基因的突变，会使体内的物质代谢成尿酸，尿酸浓度过高，容易在膀胱中形成结石，当结石随着尿液从膀胱流经尿道时，可能会造成疼痛和发炎。
                                        家中的狗狗若有出现排尿困难或膀胱发炎的症状，都可能与HUU有关，其他症状包括尿中带血或频尿。若尿酸结石卡在尿道中，无法顺利排出，则需进行手术将结石移除，若结石造成尿路完全阻塞，则可能会造成动物生命危险，
                                        应谨慎以对。引此，育种前建议先进行基因评估的检测。
                                    </p>
                                </li>
                                <li>
                                    <h2>4.	肌小管病变 </h2>
                                    <h4>Centronuclear Myopathy</h4>
                                    </br>
                                    <p>肌小管病变(CNM)过去又被称为遗传性肌病(Hereditary myopathy)，是一种体染色体隐性突变的疾病，常导致拉布拉多犬肌肉功能不足。</p>
                                    </br>
                                    <p>幼犬刚出生时外表通常无异状，但很快的症状便会开始变得明显。小狗可能因食道肌肉张力不足，导至体重一直迟迟无法增加，在2-5个月，症状通常都会逐渐明显，出现的症状包括失去肌肉张力丶无法正常控制肌肉丶异常的步态和运动不耐，且症状会随着气温降低而恶化。</p>
                                    </br>
                                    <p>不幸的是，目前针对CNM并没有治疗的方法，且罹患CNM的狗，寿命并不会因此明显变短，因此，狗狗将终身受此疾病的困扰。</p>
                                </li>
                                <li>
                                    <h2>5.	运动诱发虚脱症 </h2>
                                    <h4>Exercise-Induced Collapse</h4>
                                    </br>
                                    <p>运动诱发虚脱症(EIC)是一种体染色体隐性遗传性疾病，在帕金猎犬(Boykin Spaniel)丶乞沙比克海湾猎犬(Chesapeake Bay Retriever)丶拉布拉多(Labrador Retriever)等品种较常见。通常是指因激烈运动後，肌肉出现无法控制的症状。有此对基因突变的狗，
                                        会在犬只约5个月大至3岁间陆续出现症状，可能与这段期间是狗活动力大幅增加的时期有关。</p>
                                    </br>
                                    <p>EIC的症状，一般会在5-25分钟激烈运动後出现。并非所有类型的运动都可诱发EIC， 一般来说，犬只必须激烈奔跑很长一段时间才会出现EIC的症状，一开始可能会出现摇摆头部，後腿变得逐渐无力，最终虚脱。EIC发作的严重程度不同，也和品种不同有关，
                                        有些还能挣扎或拖着後腿行走，其他则可能完全无法移动。这些发作通常持续约5-25分钟，但是在某些较严重的病患，狗可能会在发作的当下立即死亡。</p>
                                    <br>
                                    <p>EIC的病患，并不一定会造成生活品质的下降，但主人必须注意哪些活动会造成狗狗引发EIC，需要在生活中避免从事相关的活动。</p>
                                </li>
                                <li>
                                    <h2>6.	遗传性鼻角化不全症 </h2>
                                    <h4>Hereditary Nasal Parakeratosis</h4>
                                    </br>
                                    <p>遗传性鼻角化不全症(HNPK)是拉布拉多(Labrador Retriever)的一种体染色体隐性遗传疾病。这种疾病的症状一般出现在6至12月龄的狗。</p>
                                    <br>
                                    <p>罹患此疾病的狗，在鼻子的表面和边缘会出现乾燥丶粗糙， 且略带灰褐色的硬皮。在某些病患中，鼻尖的周围会出现裂纹，如果不给予治疗，可能会造成浅层细菌感染，随着时间变长，鼻子可能会出现脱色丶颜色变淡的状况。</p>
                                    <br>
                                    <p>虽然此疾病并不会造成立即性的生命危害，但仍须终身照护，避免鼻头的硬皮过度增生或因裂纹造成的细菌感染。</p>
                                </li>
                                <li>
                                    <h2>7.	恶性高热 </h2>
                                    <h4>Malignant Hyperthermia</h4>
                                    </br>
                                    <p>恶性高热(MH)是一种体染色体显性遗传疾病，在边境牧羊犬(Border Collie)丶史宾格犬(English Springer Spaniel)丶格雷伊猎犬(Greyhound)丶爱尔兰猎狼犬(Irish Wolfhound)和拉布拉多 (Labrador Retriever)较常见。罹患此疾病的狗，会因为接触到某些触发因子，出现危害生命的生理反应。触发因子可能包括某些特定的药物，其中最常见的就是吸入性麻醉剂〈如halothane〉丶食品成分〈如咖啡因和啤酒花〉或过度运动。MH又被称为犬只紧迫症(canine stress syndrome)，即当犬只在紧迫下或受到过度刺激时也可能会出现。当犬只有MH基因突变时，手术麻醉时若接触到会触发MH的麻醉药物或其他药物，可能会有死亡的风险。</p>
                                    </br>
                                    <p>虽然并无针对MH的治疗，但可藉由确认犬只是否带有MH的突变基因，避免术前或术中接触到可能触发MH的药物，进而避免MH的发生。除此之外，也可藉由避免过度运动或处於过度紧迫的环境，避免MH的发生。</p>
                                </li>
                                <li>
                                    <h2>8.	神经性蜡样脂褐质沈着症 </h2>
                                    <h4>Neuronal Ceroid Lipofuscinosis</h4>
                                    </br>
                                    <p>神经性蜡样脂褐质沈着症(NCL)为体染色体隐性遗传疾病，是一种致命的神经系统疾病，其致病基因导致神经元中产生有缺陷的蛋白质，造成大脑组织内部废物堆积，进而造成神经元的功能受到破坏。这种遗传疾病出现在英国雪达犬（Irish Setter）丶边境牧羊犬（Border Collie）。临床症状包括行为的改变，例如侵略丶抑郁丶迟钝丶学习行为的丧失丶共济失调丶震颤丶四肢麻痹等。也因为病情会逐渐恶化，致使药物仍无法改善治疗，患犬一般於三岁左右死亡。</p>
                                </li>
                                <li>
                                    <h2>9.	多重抗药性基因　</h2>
                                    <h4>Multi-drug Resistance</h4>
                                    </br>
                                    <p>多重抗药性基因(MDR)为体染色体隐性遗传疾病，好发品种有德国狼犬(German Shepherd Dog) 丶 惠比特犬(Whippet) 丶喜乐蒂牧羊犬(Sheltie) 丶英国古代牧羊犬(Canis lupus familiaris) 丶边境牧羊犬(BorderCollie)，以及各种牧羊犬品种。</p>
